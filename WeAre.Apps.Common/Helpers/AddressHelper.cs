        public static string GetTownFromZipCode(string zipCode)
        {
            string match = FinlandTownsAndZipCodes
                .FirstOrDefault(x => x.Value.Any(row => row.Equals(zipCode, StringComparison.InvariantCultureIgnoreCase)))
                .Key;

            return match?.ToLowerInvariant();
        }

        public static string[] GetZipCodesFromTown(string town)
        {
            List<string> values = FinlandTownsAndZipCodes[town];

            return values.ToArray();
        }

